package client.monstergame.view.utils;

/**
*
* Dies ist eine OpenGl-Hilfsklasse.
*
*/
public class Vector3f {

	public float x,y,z;

	public Vector3f(float x, float y, float z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

	public Vector3f() {
		x = 0;
		y = 0;
		z = 0;
	}




}
